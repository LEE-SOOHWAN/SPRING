package com.care.dto;

public class BankDTO {
	private int num;
	private int money;
	private int totmoney;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public int getTotmoney() {
		return totmoney;
	}
	public void setTotmoney(int totmoney) {
		this.totmoney = totmoney;
	}
}
